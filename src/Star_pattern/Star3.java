package Star_pattern;

public class Star3 
{
	// * * * * * *
	// * * * * * *
	// * * * * * *
	// * * * * * *
	// * * * * * *
	
	public static void main(String[] args) 
	{
		for(int a=1; a<=5; a++) //outer for loop for rows
		{
			
			for(int b=1; b<=6; b++) //inner for loop for column
			{
				
				System.out.print("*"+" ");
			}
			System.out.println();
			
			
		}
		
	}

}
